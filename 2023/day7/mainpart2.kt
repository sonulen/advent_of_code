import java.lang.Integer.max

val cards: Map<String, Int> = arrayOf("A", "K", "Q", "T", "9", "8", "7", "6", "5", "4", "3", "2", "J",)
    .mapIndexed { index, card -> card to index }
    .toMap()

enum class Type {
    FIVE_OF_A_KIND,
    FOUR_OF_A_KIND,
    FULL_HOUSE,
    THREE_OF_A_KIND,
    TWO_PAIR,
    ONE_PAIR,
    HIGH_CARD,
    EMPTY,
}

fun rang(hand: String): Int {
    val combinationMap: Map<Char, Int> = hand.groupingBy { it }.eachCount()
    val combinations: List<Int> = combinationMap.filterKeys { it != 'J' }.values.sortedDescending()
    val jokersCount: Int = combinationMap.getOrDefault('J', 0)
    val first = combinations.getOrElse(0) { 0 } + jokersCount
    val next = combinations.getOrElse(1) { 0 }

    return when (first) {
        5 -> Type.FIVE_OF_A_KIND
        4 -> Type.FOUR_OF_A_KIND
        3 -> if (next >= 2) Type.FULL_HOUSE else Type.THREE_OF_A_KIND
        2 -> if (next >= 2) Type.TWO_PAIR else Type.ONE_PAIR
        1 -> Type.HIGH_CARD
        else -> Type.EMPTY
    }.ordinal
}

fun compareHands(left: String, right: String): Int {
    for (i in 0 until max(left.length, right.length)) {
        val lF = left.getOrNull(i)?.toString() ?: ""
        val rF = right.getOrNull(i)?.toString() ?: ""
        val first = cards[lF] ?: 0
        val second = cards[rF] ?: 0
        if (first != second) return first - second
    }
    return 0
}

fun part2(hands: Map<String, Int>): Int {
    return hands
        .toSortedMap(compareBy(::rang).thenComparator(::compareHands))
        .values.reversed()
        .mapIndexed { index, cost -> (index + 1) * cost }
        .sum()
}

